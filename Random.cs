using System;

namespace random
{
    class program 
    {
       Public static void(string[], args)
       {
          Random _random = new Random();  

          public int RandomNumber(int min, int max)  
          {  
               return _random.Next(min, max);  
          } 
           
          Console.WriteLine(RandomNumber(8, 5)); 
        }
     }
}
